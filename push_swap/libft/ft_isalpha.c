/*
* ft_isalpha
*
* Checks if the value a is from the alphabet
* Returns 1 if it is
*/

int	ft_isalpha(int a)
{
	if ((a >= 65 && a <= 90) || (a >= 97 && a <= 122))
		return (1);
	return (0);
}
